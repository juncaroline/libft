#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		l;
	int		i;
	char	*s2;

	l = 0;
	while (s[l] != '\0')
		l++;
	s2 = (char *)malloc(sizeof(char) * (l + 1));
	if (s2 == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		s2[i] = s[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
